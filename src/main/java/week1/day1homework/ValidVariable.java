public class ValidVariable {

	public void variablecheck()
	{
		int __number=34;
		int num_ber=222;
		int number__=324;
		// Except _(Underscore) other special characters are not available in variable name
		System.out.println("number is :"+ __number);
		System.out.println("number is :"+ num_ber);
		System.out.println("number is :"+ number__);
	}
}


